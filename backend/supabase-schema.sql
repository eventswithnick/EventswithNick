-- Events with Nick v7.4 stable secure database schema
-- Run this in Supabase SQL Editor once before uploading the v7.4 files.
-- Public website visitors can submit bookings.
-- Only authenticated admin users created in Supabase Authentication can read/manage admin data.
-- Recommended: in Supabase Authentication settings, keep sign-ups disabled and create Nick's admin user manually.

-- 1) Tables
create table if not exists public.bookings (
  id text primary key,
  "dateCreated" timestamptz default now(),
  name text not null,
  phone text not null,
  email text,
  "preferredCommunication" text,
  "altContact" text,
  "eventType" text,
  "eventDate" date,
  guests text,
  location text,
  package text,
  budget text,
  message text,
  status text default 'New',
  priority text default 'Normal',
  notes text,
  "quoteAmount" numeric,
  "assignedTo" text default 'Nick'
);

alter table public.bookings add column if not exists "preferredCommunication" text;
alter table public.bookings add column if not exists "altContact" text;
alter table public.bookings add column if not exists status text default 'New';
alter table public.bookings add column if not exists priority text default 'Normal';
alter table public.bookings add column if not exists notes text;
alter table public.bookings add column if not exists "quoteAmount" numeric;
alter table public.bookings add column if not exists "assignedTo" text default 'Nick';

create table if not exists public.site_settings (
  id text primary key default 'main',
  settings jsonb not null,
  updated_at timestamptz default now()
);

create table if not exists public.quote_pricing (
  id text primary key default 'main',
  pricing jsonb not null,
  updated_at timestamptz default now()
);

create table if not exists public.banking_details (
  id text primary key default 'main',
  banking jsonb not null,
  updated_at timestamptz default now()
);

create table if not exists public.gallery_items (
  id bigint generated by default as identity primary key,
  title text not null,
  category text not null,
  image text not null,
  created_at timestamptz default now()
);

-- Kept only for compatibility with older builds. v7.4 does not depend on this table.
create table if not exists public.admin_users (
  email text primary key,
  user_id uuid unique,
  display_name text,
  is_active boolean not null default true,
  created_at timestamptz default now()
);


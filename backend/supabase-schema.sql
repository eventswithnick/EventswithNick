-- 4) Public website permissions
create policy "Public can create bookings" on public.bookings
for insert
to anon, authenticated
with check (
  coalesce(status, 'New') = 'New'
  and coalesce(priority, 'Normal') = 'Normal'
  and "quoteAmount" is null
);

create policy "Public can read site settings" on public.site_settings
for select
to anon, authenticated
using (true);

create policy "Public can read gallery" on public.gallery_items
for select
to anon, authenticated
using (true);


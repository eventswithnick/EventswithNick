-- Admin allow-list. Add Nick's Supabase Auth email here after creating the Auth user.
create table if not exists public.admin_users (
  email text primary key,
  user_id uuid unique,
  display_name text,
  is_active boolean not null default true,
  created_at timestamptz default now()
);

-- 2) RLS helper. The owner of this function can read admin_users to check the logged-in user.
create or replace function public.is_events_with_nick_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.admin_users au
    where au.is_active = true
      and (
        au.user_id = auth.uid()
        or lower(au.email) = lower(coalesce(auth.jwt() ->> 'email', ''))
      )
  );
$$;

revoke all on function public.is_events_with_nick_admin() from public;
grant execute on function public.is_events_with_nick_admin() to anon, authenticated;


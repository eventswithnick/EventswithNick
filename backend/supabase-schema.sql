-- 5) Public booking policy: visitors can submit enquiries only.
create policy "Public can create bookings" on public.bookings
for insert
to anon, authenticated
with check (
  coalesce(status, 'New') = 'New'
  and coalesce(priority, 'Normal') = 'Normal'
  and "quoteAmount" is null
);

-- 6) Admin booking policies: only approved admins can manage bookings.
create policy "Admin can read bookings" on public.bookings
for select
to authenticated
using (public.is_events_with_nick_admin());

create policy "Admin can update bookings" on public.bookings
for update
to authenticated
using (public.is_events_with_nick_admin())
with check (public.is_events_with_nick_admin());

create policy "Admin can delete bookings" on public.bookings
for delete
to authenticated
using (public.is_events_with_nick_admin());


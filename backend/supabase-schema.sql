-- Events with Nick v7 secure production database schema
-- Run this in Supabase SQL Editor before uploading the v7 files to GitHub Pages.
-- This version protects the owner portal with Supabase Auth + Row Level Security.
-- Public visitors can submit booking requests, but only approved admins can read/update/delete bookings.

-- 1) Tables
create table if not exists public.bookings (
  id text primary key,
  "dateCreated" timestamptz default now(),
  name text not null,
  phone text not null,
  email text,
  "preferredCommunication" text,
  "altContact" text,
  "eventType" text,
  "eventDate" date,
  guests text,
  location text,
  package text,
  budget text,
  message text,
  status text default 'New',
  priority text default 'Normal',
  notes text,
  "quoteAmount" numeric,
  "assignedTo" text default 'Nick'
);

